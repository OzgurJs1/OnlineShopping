    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (isEmpty()) {
            int id = Integer.parseInt(jTextField1.getText());
            if (cat.isIDExists(id)) {
                if (!Check()) {
                    String cName = jTextField2.getText();
                    String description = jTextField3.getText();
                    cat.update(id, cName, description);
                    jTable1.setModel(new DefaultTableModel(null, new Object[]{"Category ID", "Category Name",
                        "Description"}));
                    cat.getCategoriesValue(jTable1, "");
                    Clear();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Category ID doesn't exists.", "Warning", 2);
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

