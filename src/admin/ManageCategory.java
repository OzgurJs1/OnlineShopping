    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a category.","Warning",2);
        }else{
            int id = Integer.parseInt(jTextField1.getText());
            if (cat.isIDExists(id)) {
                cat.delete(id);
                this.dispose();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private boolean Check() {
        String newCategory = jTextField2.getText();
        String oldCategory = model.getValueAt(rowIndex, 1).toString();

        if (newCategory.equals(oldCategory)) {
            return false;
        } else {
            if (!newCategory.equals(oldCategory)) {
                boolean x = cat.isCategoryNameExists(newCategory);
                if (x) {
                    JOptionPane.showMessageDialog(this, "This category name already exists.", "Warning", 2);
                }
                return x;
            }
        }
        return false;
    }

